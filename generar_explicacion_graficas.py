doc.add_paragraph('2. Se llama a generar_visualizaciones(resultados, df) que lee esos datos y produce las 5 imagenes PNG.')
doc.add_paragraph('3. Las imagenes se insertan despues en el reporte Word mediante doc.add_picture() dentro de generar_reporte_word().')
